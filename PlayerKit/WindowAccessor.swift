import AppKit
import SwiftUI

// When we attach the custom full screen handler
// we need access to the window which is not
// accessible through SwiftUI therefore we must
// go raw and deep inside to expose it.
struct WindowAccessor: NSViewRepresentable {
    let callback: (NSWindow) -> Void

    func makeNSView(context: Context) -> NSView {
        let view = NSView()
        DispatchQueue.main.async {
            if let window = view.window {
                callback(window)
            }
        }
        return view
    }

    func updateNSView(_ nsView: NSView, context: Context) {}
}
